//a program that prints the square and cube of the numbers from 1 to 4
public class Main
{
    public static void main(String[] args)
    {
        System.out.println("a    a^2    a^3");                     //header
        System.out.println("1    " + 1*1 + "     " + 1*1*1 );      //for n=1
        System.out.println("2    " + 2*2 + "     " + 2*2*2 );      //for n=2
        System.out.println("3    " + 3*3 + "     " + 3*3*3 );      //for n=3
        System.out.println("4    " + 4*4 + "    " + 4*4*4 );       //for n=4
    }
}
